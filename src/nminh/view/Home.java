    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
         fileChooser=new JFileChooser();
        fileChooser.setCurrentDirectory(new File("."));
        fileChooser.setDialogTitle("Select Floder");
        fileChooser.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        fileChooser.setAcceptAllFileFilterUsed(false);
        if(fileChooser.showOpenDialog(this)==JFileChooser.APPROVE_OPTION){
            String s=fileChooser.getCurrentDirectory().toString()+"\\"+fileChooser.getSelectedFile().getName();
            jLabel6.setText(s);
            jLabel7.setText("Saving....");
        }else jLabel6.setText("No Selection");
                
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jTextField1KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField1KeyReleased
        // TODO add your handling code here:
        String name=jTextField1.getText();
        System.out.println(name);
        List<String> list=new ArrayList<>();
        if(!name.equals("")||!name.equals(null)){
        try {
           list=(List<String>) mainController.search(name, driver);
        } catch (Exception ex) {
            Logger.getLogger(Home.class.getName()).log(Level.SEVERE, null, ex);
        }
        }
        showList(list);
    }//GEN-LAST:event_jTextField1KeyReleased

