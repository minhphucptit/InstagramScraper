    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        
        if(jLabel6.getText().equals("")||jLabel6.getText().equals(null)){
          jLabel7.setText("No selection");
        }else{
            try{
                File file;
                file=fileChooser.getSelectedFile();
                String path=file.getAbsolutePath();
                String name=jLabel3.getText();
                //jLabel7.setText("Dowloading....");
                Set<String> links=mainController.getImgs(driver);
                int size=mainController.save(links, path,name);
                jLabel7.setText(size+" images save successfully!");
            }catch(Exception e){
                jLabel7.setText("Dowload Failed");
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

